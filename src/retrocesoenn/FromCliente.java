public void pintar(String estado){
    
    if (estado.equals("true")){
    if(this.caracteres!=null){
            jpanel.getComponents()[conteo].setBackground(Color.green);
            jpanel.repaint();
            conteo++;
        }
    }else {
        
        if(this.caracteres!=null){
        
            jpanel.getComponents()[conteo].setBackground(Color.red);
            jpanel.repaint();
            conteo++;
        }
    } 
    
}
