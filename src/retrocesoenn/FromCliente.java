    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
       this.cmdBorrarActionPerformed(evt);
         
        //recibe un array de string del longitud = a la cantidad de cpus q tenga la pc q contiene el consumo para cada cpu
        caracteres = this.txtMensajesRecibidos.getText().toCharArray();
        JTextField [] campos = new JTextField[caracteres.length];
        //creo un array de los jTextField del tamaño del array anterior
       int x=1;
       int x2=1;
       int x3=1;
       int y=352;
        for (int i = 0; i < caracteres.length; i++) {
            
            
            String aux = String.valueOf(caracteres[i]);
            campos[i]= new JTextField();
            campos[i].setText(aux);
            campos[i].setName(i+"");
            campos[i].setBackground(Color.WHITE);
            if(i<22){
                campos[i].setBounds(29*(x),y, 30, 30);
                x=x+1;
            }if (i>23&&i<46){
               
                y=390;
                campos[i].setBounds(29*(x2),y, 30, 30);
                 x2=x2+1;
            }if (i>47&&i<70){
               
                y=428;
                campos[i].setBounds(29*(x3),y, 30, 30);
                 x3=x3+1;
            }
            
            campos[i].setBorder(border);
            campos[i].setHorizontalAlignment(SwingConstants.CENTER);                
            
            jpanel.add(campos[i]);
            jpanel.repaint();
           
        }
        
        this.cmdBorrar.setEnabled(false);
        this.btnEnviar.setEnabled(false);
        this.cmdDañada.setEnabled(false);
        
        
        try {
            this.cliente.conectar();
        } catch (IOException ex) {
            Logger.getLogger(FromCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
           
    }//GEN-LAST:event_btnEnviarActionPerformed

