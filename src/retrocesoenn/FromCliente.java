public void limpiar(){
    System.out.println(this.jpanel.getComponentCount()+" -- Componentes1111");
    if(this.caracteres!=null){
        System.out.println(this.jpanel.getComponentCount()+" -- Componentes");
        System.out.println((23+this.caracteres.length)+" -- Caracteres");
        int max=this.jpanel.getComponentCount();
        for(int i = 23 ; i<max;i++){
            jpanel.remove(jpanel.getComponents()[23]);
            jpanel.repaint();
        }
        this.caracteres=null;
        }
    this.conteo=23;
}
public void limpiar2(int pos){
    int adicional=this.caracteres.length-pos;
    if(this.caracteres!=null){
        for(int i = (23+adicional) ; i<(this.caracteres.length+adicional);i++){
            jpanel.remove(jpanel.getComponents()[i]);
            jpanel.repaint();
        }
        this.caracteres=null;
        }
    this.conteo=23;
}
